package org.example.FuncitonTest;

public class FunctionTest {
    public static void main(String[] args) {
        System.out.println("아침에 일어난다");
        //함수로 관련된 문장을 하나로 묶을 수 있음.
    }
    // void는 반환값이 없음.
    // 함수가 반환값이 있으면
    // 반환형 함수이름()
    public static void 교육() {
        System.out.println();
    }
}
